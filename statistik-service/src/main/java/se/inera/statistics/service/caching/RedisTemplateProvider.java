package se.inera.statistics.service.caching;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.data.redis.connection.RedisStandaloneConfiguration;
import org.springframework.data.redis.connection.jedis.JedisConnectionFactory;
import org.springframework.data.redis.core.RedisTemplate;
import org.springframework.data.redis.serializer.JdkSerializationRedisSerializer;
import org.springframework.data.redis.serializer.StringRedisSerializer;

public class RedisTemplateProvider {

    @Value("${redis.port:6379}")
    private String port;

    @Value("${redis.host:localhost}")
    private String host;

    public org.springframework.data.redis.core.RedisTemplate create() {
        final RedisTemplate<String, Object> redisTemplate = new RedisTemplate<>();
        final Integer portInt = Integer.valueOf(this.port);
        final RedisStandaloneConfiguration standaloneConfig = new RedisStandaloneConfiguration(host, portInt);
        final JedisConnectionFactory connectionFactory = new JedisConnectionFactory(standaloneConfig);
        redisTemplate.setConnectionFactory(connectionFactory);
        redisTemplate.setKeySerializer(new StringRedisSerializer());
        redisTemplate.setValueSerializer(new JdkSerializationRedisSerializer());
        redisTemplate.setHashKeySerializer(new StringRedisSerializer());
        redisTemplate.setHashValueSerializer(new JdkSerializationRedisSerializer());
        redisTemplate.setEnableTransactionSupport(true);
        redisTemplate.afterPropertiesSet();
        return redisTemplate;
    }

}
