package se.inera.auth;

import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.web.filter.GenericFilterBean;

import javax.servlet.FilterChain;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import java.io.IOException;

public class ForceLocalLogoutFilter extends GenericFilterBean {

    /**
     * Will clear the security context in the same way as SecurityContextLogoutHandler does it.
     * This could for instance be used to make sure that the use will get the chance to login
     * again at säkerhetstjänsten even when already logged in.
     */
    @Override
    public void doFilter(ServletRequest request, ServletResponse response, FilterChain chain) throws IOException, ServletException {
        SecurityContextHolder.clearContext();
        chain.doFilter(request, response);
    }

}
